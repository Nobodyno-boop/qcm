<?php

namespace Vroom\Orm\Decorator;

#[\Attribute(\Attribute::TARGET_CLASS)]
class Entity
{
    private string $name;

    /**
     * @param string $name
     */
    public function __construct(string $name)
    {
        $this->name = $name;
    }

    /**
     * @return string
     */
    public function getName(): string
    {
        return $this->name;
    }

}